const a = 7
let b = 3


b += a // b = b + a (atribuição aditiva)
console.log(b)

b -= 4 // b = b - a (atribuição subtrativa)
console.log(b)

b *= 2 // b = b * 2 (atribuição multiplicativa)
console.log(b)

b /= 2 // b = b / 2 (atribuição divisiva)
console.log(b)

b %=2 // b = b % 2 (resto da divisao)
console.log(b)